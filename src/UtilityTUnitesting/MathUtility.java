package UtilityTUnitesting;


/**
 * A library of arithmetic methods
 */
public class MathUtility{

    /**
     * A method to compute the sum of two integers
     * @param num1 the first integer
     * @param num2 the second integer
     * @return the sum of the two integers
     */

    public static int add(int num1, int num2){
        return num1 + num2;
    }

    /**
     * A method to computer the difference of two integers
     * @param number1 the first integer
     * @param number2 the second integer
     * @return the difference of the two integers
     */

     public static int subtract(int number1, int number2){
        return number1 - number2;
     }

    /**
     * A method to computer the product of two integers
     * @param number1 the first integer
     * @param number2 the second integer
     * @return the product of the two integers
     */

     public static int multiply(int number1, int number2){
        return number1 * number2;
     }
}